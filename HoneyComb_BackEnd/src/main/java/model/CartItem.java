package model;

public class CartItem {
    private int amount;
    private int book;

    public int getAmount() {
        return amount;
    }

    public int getBook() {
        return book;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public void setBook(int book) {
        this.book = book;
    }
}
